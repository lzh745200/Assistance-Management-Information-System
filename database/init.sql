-- ============================================================
-- 创建索引（SQLite 不会自动为外键创建索引）
-- 注意：SQLite 不支持 ON UPDATE CURRENT_TIMESTAMP，
-- 所有表的 updated_at 由应用层（SQLAlchemy onupdate）管理
-- ============================================================
-- 唯一约束索引
CREATE INDEX IF NOT EXISTS idx_villages_code ON villages(code);
CREATE INDEX IF NOT EXISTS idx_projects_code ON projects(code);

-- 外键索引（连接查询性能关键）
CREATE INDEX IF NOT EXISTS idx_users_role ON users(role_id);
CREATE INDEX IF NOT EXISTS idx_schools_village ON schools(village_id);
CREATE INDEX IF NOT EXISTS idx_projects_village ON projects(village_id);
CREATE INDEX IF NOT EXISTS idx_funds_project ON funds(project_id);
CREATE INDEX IF NOT EXISTS idx_funds_approved_by ON funds(approved_by);
CREATE INDEX IF NOT EXISTS idx_work_logs_user ON work_logs(user_id);
CREATE INDEX IF NOT EXISTS idx_work_logs_village ON work_logs(village_id);
CREATE INDEX IF NOT EXISTS idx_data_sync_logs_created_by ON data_sync_logs(created_by);
CREATE INDEX IF NOT EXISTS idx_audit_logs_user ON audit_logs(user_id);

-- 查询过滤索引（常用过滤/排序字段）
CREATE INDEX IF NOT EXISTS idx_villages_status ON villages(status);
CREATE INDEX IF NOT EXISTS idx_users_email ON users(email);
CREATE INDEX IF NOT EXISTS idx_users_is_active ON users(is_active);
CREATE INDEX IF NOT EXISTS idx_projects_status ON projects(status);
CREATE INDEX IF NOT EXISTS idx_projects_type ON projects(project_type);
CREATE INDEX IF NOT EXISTS idx_funds_status ON funds(status);
CREATE INDEX IF NOT EXISTS idx_funds_type ON funds(fund_type);
CREATE INDEX IF NOT EXISTS idx_work_logs_date ON work_logs(log_date);
CREATE INDEX IF NOT EXISTS idx_data_sync_logs_status ON data_sync_logs(status);
CREATE INDEX IF NOT EXISTS idx_audit_logs_action ON audit_logs(action);
CREATE INDEX IF NOT EXISTS idx_audit_logs_created ON audit_logs(created_at);

-- 复合索引（常用多条件查询）
CREATE INDEX IF NOT EXISTS idx_audit_logs_user_action_time ON audit_logs(user_id, action, created_at);

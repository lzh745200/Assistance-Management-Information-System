/** 贵州省 9 个市州及下辖县市区（兼容旧版） */
export const GUIZHOU_REGION: LegacyCityData[] = GUIZHOU_REGION_FULL.map((c) => ({
  name: c.name,
  counties: c.counties.map((co) => co.name),
}))

/** 贵州省全部分县市（88个）flat 列表 */
export const GUIZHOU_ALL_COUNTIES: string[] = GUIZHOU_REGION_FULL.flatMap((c) =>
  c.counties.map((co) => co.name)
)

/** 贵州省全部分市州名称 */
export const GUIZHOU_ALL_CITIES: string[] = GUIZHOU_REGION_FULL.map((c) => c.name)

/** 根据市州名称获取下属县区列表 */
export function getCountiesByCity(cityName: string): string[] {
  const city = GUIZHOU_REGION_FULL.find((c) => c.name === cityName)
  return city ? city.counties.map((co) => co.name) : []
}

/** 根据市州和县区获取下属乡镇列表 */
export function getTownshipsByCityCounty(cityName: string, countyName: string): string[] {
  const city = GUIZHOU_REGION_FULL.find((c) => c.name === cityName)
  if (!city) return []
  const county = city.counties.find((co) => co.name === countyName)
  return county ? county.townships : []
}

